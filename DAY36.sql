(JERSY_NO  numeric(3) CHECK(JERSY_NO<0) PRIMARY KEY,
PNAME VARCHAR(30) NOT NULL,
RUNS NUMERIC(4),
WICKETS numeric(3),
CATCHES NUMERIC(2),
AVG NUMERIC(4,2),
POTM NUMERIC(3)
);

CREATE TABLE DOMESTIC_PER
(   PNAME VARCHAR(30) NOT NULL,
    RUNS NUMERIC(4),
    WICKETS NUMERIC(3),
    CATCHES NUMERIC(2),
    AVG NUMERIC(4,2),
    POTM NUMERIC(3),
    JERSY_NO NUMERIC(3),
    CONSTRAINT FRKEY FOREIGN KEY (JERSY_NO) REFERENCES INTERNATIONAL_PER(JERSY_NO)
