-- FINDING NTH MAX AND MIN SALARY BY USING PSUDOCOLUMNS:

-- WAQTD THIRD MAXIMUM SALARY
 SELECT SAL 
 FROM (SELECT ROWNUM AS SLNO , SAL
       FROM (SELECT DISTINCT SAL
             FROM EMP
             ORDER BY SAL DESC))
 WHERE SLNO = 3;

--    SAL
-- ------
--   2975

-- WAQTD THIRD MINIMUM SALARY
 SELECT SAL 
 FROM (SELECT ROWNUM AS SLNO , SAL
       FROM (SELECT DISTINCT SAL
             FROM EMP
             ORDER BY SAL ASC))
 WHERE SLNO = 3;
 
 
 
