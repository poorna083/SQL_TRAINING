CREATE DATABASE QSP;
USE QSP;
CREATE TABLE WINE_SHOP
(SLNO numeric(3) NOT null check(SLNO>0) primary key,
BRAND VARCHAR(30) NOT NULL,
PRICE numeric(5,2) NOT NULL  check(PRICE>0),
QUANTITY varchar(10) NOT NULL,
SIDE_DISH varchar(10),
TYPE VARCHAR(30) NOT NULL
);

SELECT * FROM WINE_SHOP;



-- RELATION TABLE
USE QSP;
CREATE TABLE CUSTOMERS
(CID numeric(3) NOT NULL primary KEY,
CNAME varchar(30) NOT NULL,
PHNO numeric(10) NOT NULL check(length(PHNO)=10),
SLNO NUMERIC (3),
constraint IRFAN_FK foreign key(SLNO) REFERENCES WINE_SHOP(SLNO)
);

SELECT * FROM CUSTOMERS;


