-- WAQTD ENAME ALONG WITH THEIR DEPARTMRNT NAME
-- ANSI:
SELECT ENAME
FROM employee.emp INNER JOIN department.dept
ON EMP.DEPTNO = dept.DEPTNO;
-- ORACLE:
SELECT ENAME 
FROM EMP , department.dept
WHERE EMP.DEPTNO = DEPT.DEPTNO;
-- WAQTD NAME,SALRY,LOCATION OF ALL THE EMPLOYEES WHO ARE EARNING SALARY MORE THAN 1500
-- ANSI:
SELECT ENAME,SAL,LOC
FROM employee.EMP INNER JOIN department.dept
ON EMP.DEPTNO = dept.DEPTNO
WHERE SAL > 1500;
-- ORACLE: 
SELECT ENAME,SAL,LOC
FROM employee.EMP INNER JOIN department.dept
WHERE EMP.DEPTNO = DEPT.DEPTNO AND SAL > 1500;
