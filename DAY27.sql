-- JOINS:
--   IT IS THE PROCESS OF RETRIVING DATA FROM MULTIPLE TABLES SIMULTINOUSLY
--   THE JOINS ARE DIVIDED INTO:
--       *CARTASION JOIN/CROSS JOIN
--       *INNER JOIN/EQUI JOIN
--       *NATURAL JOIN
--       *OUTER JOIN
--           ->LEFT OUTER JOIN
--           ->RIGHT OUTER JOIN
--           ->FULL OUTER JOIN
-- 	 *SELF JOIN
--      
--      
--      CROSS JOIN:
--          IN THESE TYPE OF JOIN THE RECORD FROM ONE TABLE WILL BE MERGED ALL THE RECORDS OF ANOTHER TABLE
--          
--          NO.OF COLUMN --->TAB1 + TAB2
--          NO.OF ROWS  ----> TAB1*TAB2
--          
--          SYNTAX:
--          ANSI:
--          SELECT */COL_NAME/EXP
--          FROM TAB_NAM1 CROSS JOIN TAB_NAME2;
--          
--          ORACLE:
--          SELECT */COL_NAME/EXP
--          FROM TAB_NAM1,TAB_NAME2;

USE EMPLOYEE;
SHOW SCHEMAS;
USE DEPARTMENT;

-- ANSI: 
SELECT *
FROM EMP CROSS JOIN department.dept;

-- ORACLE:
SELECT *
FROM EMP,department.dept;



